<h1>
  Sveltify
</h1>

<p>
  <a href="https://github.com/zdproduction/sveltify/blob/master/LICENSE" target="_blank">
    <img alt="GitHub license" src="https://img.shields.io/github/license/zdproduction/sveltify?style=plastic">
